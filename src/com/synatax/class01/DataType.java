package com.synatax.class01;

public class DataType {

	public static void main(String[] args) {
	
 //Data types and variables: 
		  
		//To represent whole numbers: 
		
		byte room1 = 12; 
		short room2 = 3223;
		int room3 = 7612; 
		long room4 = 12321321434L;
				
		 //To represent decimil numbers
				
		float container1 = 1.34f; 
		double container2 = 90.99;
				
		 //To represent one character 
				
		char shelf1 = 'm'; 
				
				
		// To represent Y/N
		
		boolean sleepy = true; 
		boolean hungry = false; 
		
//To access and print the values from all variables: 
				
System.out.println(room1);
System.out.println(room2);
System.out.println(room3);
System.out.println(room4);
System.out.println(container1);
System.out.println(container2);
System.out.println(shelf1);
System.out.println(sleepy);
System.out.println(hungry);






	}

}
